def connect_db(db_file):
    """
    Connects tot the database
    ARGS:
        db_file: the pah to the database file
    RETURNS:
        connection, which is the connection to the database. none if no connection is established
    """
    try:
        connection = sqlite3.connect(db_file)
        cursor = connection.cursor()
        print("CONN")
        return cursor, connection
    except Error as e:
        print(e)
        return none

def get_attendance_data(cur, con):

    # Move the cursor to select the data.
    #cur.execute("DELETE FROM attendance;")
    #con.commit()
    cur.execute("SELECT DISTINCT date FROM attendance;")
    # Get the all of the different dates that attendance hasbeen taken on.
    dates = cur.fetchall()
    record = {}
    for day in dates:
        # For every day, now get the attendance for that day. the use of f string here is fine, as it processes data from the database which has been autogenerated. (The date)
        cur.execute(f"SELECT date, present, personID FROM attendance WHERE date = {day[0]} ORDER BY personID ;")
        record_day = cur.fetchall()

        # Copies the datastructute to allow for changes during itteration.
        rd_copy = deepcopy(record_day)
        for i, person in enumerate(record_day):
            cur.execute(f"SELECT fname, lname, role FROM people WHERE id = {person[2]}")
            person_data = cur.fetchall()
            # Append the person data to the attendance data by creating a new tuple
            rd_copy[i] = (person[0], person[1], person_data)
        # Adds all of the person and attendance data just collected to the 'record' dict to be returned in html
        record[day[0]] = rd_copy
    # close the connection when done, prevents issues like memory leaks
    return record

