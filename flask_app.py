@app.route('/best', methods=["POST"])
def best():
    """Presents the results of who is present
    """
    if request.form.get("dummy") == "1":
        # For not overwriting data when demonstrating
        return redirect("/")
    else:
        today = "2" + datetime.now(pytz.timezone("Pacific/Auckland")).strftime("%d%m%Y")

        cur, con = connect_db(DB)


        cur.execute("SELECT id, fname, lname, role FROM people")
        current_attendance = cur.fetchall()
        # Restructures the returned data from a list to a dict with a key of th id.
        attendance_dict = {p[0]: list(p[1:]) for p in current_attendance}

        # Copy the data so we dont delete items from the dict we are itterating through while we itterate through it.
        ad = deepcopy(attendance_dict)

        # The whole loop is to add the people who were not there, as the data returned from the form does not include unmarked check boxes.
        for actor, data in attendance_dict.items():
            if str(actor) in dict(request.form):
                # If the actor was present.
                ad[actor].append(True)
            elif request.form.get("or") == "1":
                # If the user submited 'or' then
                del ad[actor]
            else:
                ad[actor].append(False)
        attendance_dict = ad
        insert_data = [(f"{today}_{id}", today, attendance_dict[id][3], id) for id in attendance_dict.keys()]

        # Insert or replace is used for both updating data with more recent attendance data and for adding new data for new days
        cur.executemany("INSERT OR REPLACE INTO attendance(itemID, date, present, personID) VALUES (?, ?, ?, ?)", insert_data)

        con.commit()
        con.close()
        scene_scores = {}
        for scene, chars in lines.items():
            temp_score = 0
            for id, char_dat in attendance_dict.items():
                if char_dat[3] and char_dat[2] in chars:
                    # Add the number of lines
                    temp_score += chars[char_dat[2]]
            scene_scores[scene] = temp_score/chars["total"]
    #scene_scores
        return render_template("best.html", scenes=dict(sorted(scene_scores.items(), key=lambda item: item[1], reverse=True)))


